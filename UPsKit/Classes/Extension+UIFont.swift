import UIKit

extension UIFont {
  
  // MARK: - SinhalaSangam
  // 타이머 전용 폰트
  
  public class SinhalaSangam {
    public class func bold(size: CGFloat) -> UIFont {
      UIFont(name: "SinhalaSangamMN-Bold", size: size)!
    }
  }
  
}
